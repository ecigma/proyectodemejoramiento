#include <stdio.h>
#include <iostream>
#include <fstream>
using namespace std;
/*==============================================================================
Variables
==============================================================================*/
int V[100];
/*==============================================================================
Definición de Metodos
==============================================================================*/
int buscaCantidad(int n);

/*==============================================================================
Metodo principal
==============================================================================*/
int main (){
    int n, cont;
    while(cin>>n){
        for (int i=0; i<n; i++){
            cin>>V[i];
        }
        cont= buscaCantidad(n);
        cout<<"Minimum exchange operations : "<<cont<<endl;
    }
    getchar();
    return 0;
}

/*==============================================================================
Busca Candidatos
==============================================================================*/
int buscaCantidad(int n){
    int cont, aux;
    cont=0;
    for (int i=1; i<n; i++){
         for (int j=0; j<n-1; j++){
              if (V[j] > V[j+1]){
                    aux = V[j];
                    V[j] = V[j+1];
                    V[j+1] = aux;
                    cont++;
              }
         }
     }
    return cont;
}

