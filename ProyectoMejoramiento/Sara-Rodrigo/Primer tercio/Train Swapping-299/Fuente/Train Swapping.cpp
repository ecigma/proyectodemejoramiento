int buscaCantidad(int n);

/*==============================================================================
Metodo principal
==============================================================================*/
int main (){
    int cant, n, cont;
    cin>>cant;
    while (cant){
        cin>>n;
        cont= buscaCantidad(n);
        cout<<"Optimal train swapping takes "<<cont<<" swaps."<<endl;
        cant--;
    }
    return 0;
}

/*==============================================================================
Busca Candidatos
==============================================================================*/
int buscaCantidad(int n){
    int cont, aux;
    cont=0;
    for (int i=0; i<n; i++){
       cin>>V[i];
    }
       for (int i=0; i<n; i++){
            for (int j=i+1; j<n; j++){
                if (V[i] > V[j]){
                    aux = V[i];
                    V[i] = V[j];
                    V[j] = aux;
                    cont++;
                }
            }
       }
    return cont;
}

